package ejercicios.tema1;

public class Ejercicios1 {

    public static void main(String args[])
    {
        int numero1;
        long numero2;
        float numero3;
        double numero4;
        boolean logic_value;
        String str;

        numero1 = -13451;
        numero2 = 94259;

        numero3 = 3.3412f;
        numero4 = -2131.23123;

        logic_value = true;
        str = "hola que tal";

        System.out.println("int: " + numero1);
        System.out.println("long: " + numero2);

        System.out.println("float: " + numero3);
        System.out.println("double: " + numero4);

        System.out.println("boolean: " + logic_value);
        System.out.println("String: " + str);
    }
}
